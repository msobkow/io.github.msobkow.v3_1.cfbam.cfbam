public class CFBamServerObjFuncObj
	extends CFBamServerMethodObj
	implements ICFBamServerObjFuncObj
{
	protected ICFBamTableObj optionalLookupRetTable;

	public CFBamServerObjFuncObj() {
		super();
		optionalLookupRetTable = null;
	}

	public CFBamServerObjFuncObj( ICFBamSchemaObj argSchema ) {
		super( argSchema );
		optionalLookupRetTable = null;
	}

	@Override
	public int getClassCode() {
		return( ((ICFBamSchemaObj)schema).getServerObjFuncTableObj().getClassCode() );
	}

	@Override
	public String getGenDefName() {
		return( "ServerObjFunc" );
	}

	@Override
	public ICFLibAnyObj getObjScope() {
		return( super.getObjScope() );
	}

	@Override
	public String getObjName() {
		String objName;
		objName = getRequiredName();
		return( objName );
	}

	@Override
	public ICFLibAnyObj getObjQualifier( Class qualifyingClass ) {
		ICFLibAnyObj container = this;
		if( qualifyingClass != null ) {
			while( container != null ) {
				if( container instanceof ICFBamClusterObj ) {
					break;
				}
				else if( container instanceof ICFBamTenantObj ) {
					break;
				}
				else if( qualifyingClass.isInstance( container ) ) {
					break;
				}
				container = container.getObjScope();
			}
		}
		else {
			while( container != null ) {
				if( container instanceof ICFBamClusterObj ) {
					break;
				}
				else if( container instanceof ICFBamTenantObj ) {
					break;
				}
				container = container.getObjScope();
			}
		}
		return( container );
	}

	@Override
	public ICFLibAnyObj getNamedObject( String objName ) {
		String nextName;
		String remainingName;
		ICFLibAnyObj subObj = null;
		ICFLibAnyObj retObj;
		int nextDot = objName.indexOf( '.' );
		if( nextDot >= 0 ) {
			nextName = objName.substring( 0, nextDot );
			remainingName = objName.substring( nextDot + 1 );
		}
		else {
			nextName = objName;
			remainingName = null;
		}
		if( remainingName == null ) {
			retObj = subObj;
		}
		else if( subObj == null ) {
			retObj = null;
		}
		else {
			retObj = subObj.getNamedObject( remainingName );
		}
		return( retObj );
	}

	@Override
	public String getObjQualifiedName() {
		String qualName = getObjName();
		ICFLibAnyObj container = getObjScope();
		String containerName;
		while( container != null ) {
			if( container instanceof ICFSecClusterObj ) {
				container = null;
			}
			else if( container instanceof ICFSecTenantObj ) {
				container = null;
			}
			else if( container instanceof ICFBamSchemaDefObj ) {
				container = null;
			}
			else {
				containerName = container.getObjName();
				qualName = containerName + "." + qualName;
				container = container.getObjScope();
			}
		}
		return( qualName );
	}

	@Override
	public ICFBamScopeObj realise() {
		ICFBamServerObjFuncObj retobj = ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().realiseServerObjFunc(
			(ICFBamServerObjFuncObj)this );
		return( (ICFBamScopeObj)retobj );
	}

	@Override
	public void forget() {
		((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().reallyDeepDisposeServerObjFunc( (ICFBamServerObjFuncObj)this );
	}

	@Override
	public ICFBamScopeObj read() {
		ICFBamServerObjFuncObj retobj = ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().readServerObjFuncByIdIdx( getPKey(), false );
		return( (ICFBamScopeObj)retobj );
	}

	@Override
	public ICFBamScopeObj read( boolean forceRead ) {
		ICFBamServerObjFuncObj retobj = ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().readServerObjFuncByIdIdx( getPKey(), forceRead );
		return( (ICFBamScopeObj)retobj );
	}

	@Override
	public ICFBamServerObjFuncTableObj getServerObjFuncTable() {
		return( ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj() );
	}

	@Override
	public ICFBamScope getRec() {
		if( rec == null ) {
			if( isNew ) {
				rec = getSchema().getCFBamBackingStore().getFactoryServerObjFunc().newRec();
			}
			else {
				// Read the data rec via the backing store
				rec = getSchema().getCFBamBackingStore().getTableServerObjFunc().readDerivedByIdIdx( ((ICFBamSchemaObj)getSchema()).getAuthorization(),
						getPKey() );
				if( rec != null ) {
					copyRecToPKey();
				}
			}
		}
		return( rec );
	}

	@Override
	public void setRec( ICFBamScope value ) {
		if( ! ( ( value == null ) || ! ( value instanceof ICFBamServerObjFunc ) ) ) {
			throw new CFLibUnsupportedClassException( getClass(),
				"setRec",
				"value",
				value,
				"CFBamServerObjFuncRec" );
		}
		rec = value;
		copyRecToPKey();
		requiredOwnerTenant = null;
		optionalLookupDefSchema = null;
		requiredContainerForTable = null;
		optionalLookupRetTable = null;
	}

	@Override
	public ICFBamServerObjFunc getServerObjFuncRec() {
		return( (ICFBamServerObjFunc)getRec() );
	}

	@Override
	public ICFBamScopeEditObj beginEdit() {
		if( edit != null ) {
			throw new CFLibEditAlreadyOpenException( getClass(), "beginEdit" );
		}
		ICFBamServerObjFuncObj lockobj;
		if( getIsNew() ) {
			lockobj = (ICFBamServerObjFuncObj)this;
		}
		else {
			lockobj = ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().lockServerObjFunc( getPKey() );
		}
		edit = ((ICFBamSchemaObj)getSchema()).getServerObjFuncTableObj().newEditInstance( lockobj );
		return( (ICFBamScopeEditObj)edit );
	}

	@Override
	public ICFBamServerObjFuncEditObj getEditAsServerObjFunc() {
		return( (ICFBamServerObjFuncEditObj)edit );
	}

	@Override
	public ICFSecSecUserObj getCreatedBy() {
		if( createdBy == null ) {
			ICFBamScope rec = getRec();
			createdBy = ((ICFBamSchemaObj)getSchema()).getSecUserTableObj().readSecUserByIdIdx( rec.getCreatedByUserId() );
		}
		return( createdBy );
	}

	@Override
	public LocalDateTime getCreatedAt() {
		return( getRec().getCreatedAt() );
	}

	@Override
	public ICFSecSecUserObj getUpdatedBy() {
		if( updatedBy == null ) {
			ICFBamScope rec = getRec();
			updatedBy = ((ICFBamSchemaObj)getSchema()).getSecUserTableObj().readSecUserByIdIdx( rec.getUpdatedByUserId() );
		}
		return( updatedBy );
	}

	@Override
	public LocalDateTime getUpdatedAt() {
		return( getRec().getUpdatedAt() );
	}

	@Override
	public CFLibDbKeyHash256 getRequiredId() {
		return( getPKey() );
	}

	@Override
	public ICFBamTableObj getOptionalLookupRetTable() {
		return( getOptionalLookupRetTable( false ) );
	}

	@Override
	public ICFBamTableObj getOptionalLookupRetTable( boolean forceRead ) {
		if( ( optionalLookupRetTable == null ) || forceRead ) {
			boolean anyMissing = false;
			if( getServerObjFuncRec().getOptionalRetTableId() == null ) {
				anyMissing = true;
			}
			if( ! anyMissing ) {
				optionalLookupRetTable = ((ICFBamSchemaObj)getSchema()).getTableTableObj().readTableByIdIdx( getServerObjFuncRec().getOptionalRetTableId(), forceRead );
			}
		}
		return( optionalLookupRetTable );
	}

	@Override
	public CFLibDbKeyHash256 getOptionalRetTableId() {
		return( getServerObjFuncRec().getOptionalRetTableId() );
	}
}
