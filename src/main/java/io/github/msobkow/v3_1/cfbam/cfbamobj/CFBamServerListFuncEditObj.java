public class CFBamServerListFuncEditObj
	extends CFBamServerMethodEditObj

	implements ICFBamServerListFuncEditObj
{
	protected ICFBamTableObj optionalLookupRetTable;

	public CFBamServerListFuncEditObj( ICFBamServerListFuncObj argOrig ) {
		super( argOrig );
		optionalLookupRetTable = null;
	}

	@Override
	public int getClassCode() {
		return( ((ICFBamSchemaObj)orig.getSchema()).getServerListFuncTableObj().getClassCode() );
	}

	@Override
	public String getGenDefName() {
		return( "ServerListFunc" );
	}

	@Override
	public ICFLibAnyObj getObjScope() {
		return( super.getObjScope() );
	}

	@Override
	public String getObjName() {
		String objName;
		objName = getRequiredName();
		return( objName );
	}

	@Override
	public ICFLibAnyObj getObjQualifier( Class qualifyingClass ) {
		ICFLibAnyObj container = this;
		if( qualifyingClass != null ) {
			while( container != null ) {
				if( container instanceof ICFBamClusterObj ) {
					break;
				}
				else if( container instanceof ICFBamTenantObj ) {
					break;
				}
				else if( qualifyingClass.isInstance( container ) ) {
					break;
				}
				container = container.getObjScope();
			}
		}
		else {
			while( container != null ) {
				if( container instanceof ICFBamClusterObj ) {
					break;
				}
				else if( container instanceof ICFBamTenantObj ) {
					break;
				}
				container = container.getObjScope();
			}
		}
		return( container );
	}

	@Override
	public ICFLibAnyObj getNamedObject( String objName ) {
		String nextName;
		String remainingName;
		ICFLibAnyObj subObj = null;
		ICFLibAnyObj retObj;
		int nextDot = objName.indexOf( '.' );
		if( nextDot >= 0 ) {
			nextName = objName.substring( 0, nextDot );
			remainingName = objName.substring( nextDot + 1 );
		}
		else {
			nextName = objName;
			remainingName = null;
		}
		if( remainingName == null ) {
			retObj = subObj;
		}
		else if( subObj == null ) {
			retObj = null;
		}
		else {
			retObj = subObj.getNamedObject( remainingName );
		}
		return( retObj );
	}

	@Override
	public String getObjQualifiedName() {
		String qualName = getObjName();
		ICFLibAnyObj container = getObjScope();
		String containerName;
		while( container != null ) {
			if( container instanceof ICFSecClusterObj ) {
				container = null;
			}
			else if( container instanceof ICFSecTenantObj ) {
				container = null;
			}
			else if( container instanceof ICFBamSchemaDefObj ) {
				container = null;
			}
			else {
				containerName = container.getObjName();
				qualName = containerName + "." + qualName;
				container = container.getObjScope();
			}
		}
		return( qualName );
	}

	@Override
	public ICFBamScopeObj realise() {
		// We realise this so that it's record will get copied to orig during realization
		ICFBamServerListFuncObj retobj = getSchema().getServerListFuncTableObj().realiseServerListFunc( (ICFBamServerListFuncObj)this );
		return( retobj );
	}

	@Override
	public void forget() {
		getOrigAsServerListFunc().forget();
	}

	@Override
	public ICFBamScopeObj create() {
		copyRecToOrig();
		ICFBamServerListFuncObj retobj = ((ICFBamSchemaObj)getOrigAsServerListFunc().getSchema()).getServerListFuncTableObj().createServerListFunc( getOrigAsServerListFunc() );
		if( retobj == getOrigAsServerListFunc() ) {
			copyOrigToRec();
		}
		return( retobj );
	}

	@Override
	public CFBamScopeEditObj update() {
		getSchema().getServerListFuncTableObj().updateServerListFunc( (ICFBamServerListFuncObj)this );
		return( null );
	}

	@Override
	public CFBamScopeEditObj deleteInstance() {
		if( getIsNew() ) {
			throw new CFLibCannotDeleteNewInstanceException( getClass(), "delete" );
		}
		getSchema().getServerListFuncTableObj().deleteServerListFunc( getOrigAsServerListFunc() );
		return( null );
	}

	@Override
	public ICFBamServerListFuncTableObj getServerListFuncTable() {
		return( orig.getSchema().getServerListFuncTableObj() );
	}

	@Override
	public ICFBamServerListFuncEditObj getEditAsServerListFunc() {
		return( (ICFBamServerListFuncEditObj)this );
	}

	@Override
	public ICFBamServerListFuncObj getOrigAsServerListFunc() {
		return( (ICFBamServerListFuncObj)orig );
	}

	@Override
	public ICFBamScope getRec() {
		if( rec == null ) {
			rec = getOrigAsServerListFunc().getSchema().getCFBamBackingStore().getFactoryServerListFunc().newRec();
			rec.set( orig.getRec() );
		}
		return( rec );
	}

	@Override
	public void setRec( ICFBamScope value ) {
		if( rec != value ) {
			super.setRec( value );
			optionalLookupRetTable = null;
		}
	}

	@Override
	public ICFBamServerListFunc getServerListFuncRec() {
		return( (ICFBamServerListFunc)getRec() );
	}

	@Override
	public CFLibDbKeyHash256 getRequiredId() {
		return( getPKey() );
	}

	@Override
	public void setRequiredId(CFLibDbKeyHash256 id) {
		if (getPKey() != id) {
			setPKey(id);
		}
	}

	@Override
	public CFLibDbKeyHash256 getOptionalRetTableId() {
		return( getServerListFuncRec().getOptionalRetTableId() );
	}

	@Override
	public ICFBamTableObj getOptionalLookupRetTable() {
		return( getOptionalLookupRetTable( false ) );
	}

	@Override
	public ICFBamTableObj getOptionalLookupRetTable( boolean forceRead ) {
		if( forceRead || ( optionalLookupRetTable == null ) ) {
			boolean anyMissing = false;
			if( getServerListFuncRec().getOptionalRetTableId() == null ) {
				anyMissing = true;
			}
			if( ! anyMissing ) {
				ICFBamTableObj obj = ((ICFBamSchemaObj)getOrigAsServerListFunc().getSchema()).getTableTableObj().readTableByIdIdx( getServerListFuncRec().getOptionalRetTableId() );
				optionalLookupRetTable = obj;
			}
		}
		return( optionalLookupRetTable );
	}

	@Override
	public void setOptionalLookupRetTable( ICFBamTableObj value ) {
		if( rec == null ) {
			getServerListFuncRec();
		}
		if( value != null ) {
			optionalLookupRetTable = value;
			getServerListFuncRec().setOptionalLookupRetTable(value.getTableRec());
		}
		else {
			optionalLookupRetTable = null;
			getServerListFuncRec().setOptionalLookupRetTable((ICFBamTable)null);
		}
		optionalLookupRetTable = value;
	}

	@Override
	public void copyRecToOrig() {
		ICFBamServerListFunc origRec = getOrigAsServerListFunc().getServerListFuncRec();
		ICFBamServerListFunc myRec = getServerListFuncRec();
		origRec.set( myRec );
	}

	@Override
	public void copyOrigToRec() {
		ICFBamServerListFunc origRec = getOrigAsServerListFunc().getServerListFuncRec();
		ICFBamServerListFunc myRec = getServerListFuncRec();
		myRec.set( origRec );
	}
}
