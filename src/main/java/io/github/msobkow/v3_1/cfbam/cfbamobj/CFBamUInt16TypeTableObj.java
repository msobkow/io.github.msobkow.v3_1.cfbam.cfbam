	@Override
	public ICFBamUInt16TypeObj readUInt16Type( CFLibDbKeyHash256 pkey, boolean forceRead ) {
		ICFBamUInt16TypeObj obj = null;
		if( ( ! forceRead ) && members.containsKey( pkey ) ) {
			obj = members.get( pkey );
		}
		else {
			ICFBamUInt16Type readRec = schema.getCFBamBackingStore().getTableUInt16Type().readDerivedByIdIdx( null,
						pkey );
			if( readRec != null ) {
				obj = (ICFBamUInt16TypeObj)schema.getValueTableObj().constructByClassCode( readRec.getClassCode() );
				obj.setPKey( readRec.getPKey() );
				obj.setRec( readRec );
				obj = (ICFBamUInt16TypeObj)obj.realise();
			}
		}
		return( obj );
	}

