	@Override
	public ICFBamUInt16TypeObj createUInt16Type( ICFBamUInt16TypeObj Obj ) {
		ICFBamUInt16TypeObj obj = Obj;
		ICFBamUInt16Type rec = obj.getUInt16TypeRec();
		schema.getCFBamBackingStore().getTableUInt16Type().createUInt16Type(
			null,
			rec );
		obj.copyRecToPKey();
		if( obj.getClassCode() == runtimeClassCode ) {
			obj = (ICFBamUInt16TypeObj)(obj.realise());
		}
		ICFBamValueObj prev = obj.getOptionalLookupPrev();
		if( prev != null ) {
			prev.read( true );
		}
		obj.endEdit();
		return( obj );
	}

